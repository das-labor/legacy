#include <stdint.h>

extern void *conf_handler (uint_fast8_t in_idx, void* in_val)
{
	static uint_fast16_t termwidth = 80;
	static uint_fast8_t dumpmode = CONF_DUMP_HEX;
	switch (in_idx)
	{
		case CONF_TERMWIDTH:
			if (in_val != NULL) termwidth = in_val;
			return termwidth;

		case CONF_DUMPMODE:
			if (in_val != NULL) dumpmode = in_val;
			return dumpmode;
	}	
}
